public class naloga1 {
    public static void main(String[] args) {
        int celoStevilo = 2025;
        double decimalnoStevilo = 3.14;
        long dolgoStevilo = 1251234121;
        char crka = 'A';
        String besedilo = "To je besedilo.";
        boolean resnicaLaž = false;

        System.out.println("Int: " + celoStevilo);
        System.out.println("Double: " + decimalnoStevilo);
        System.out.println("Long: " + dolgoStevilo);
        System.out.println("Char: " + crka);
        System.out.println("String: " + besedilo);
        System.out.println("Boolean: " + resnicaLaž);
    }
}